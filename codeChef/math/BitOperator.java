package codeChef.math;

public class BitOperator {
    public static void main(String[] args) {

        System.out.println("a|b =" + (5 | 7));
        System.out.println("a&b =" + (5 & 7));
        System.out.println("a^b =" + (5 ^ 7));
        System.out.println("~a =" + (~5));
        System.out.println("Right shift "+ (27>>3));
        System.out.println("Left shift "+ (2<<2));
    }
}
